package com.study.JuintDemo;


public class Caculate {

    /**
     *
     * 计算两个数的和
     * @param a
     * @param b
     * @return
     */

    public int Sum(int a,int b)
    {
        return  a+b;
    }

    /**
     * 计算两个数的差
     * @param a
     * @param b
     * @return
     */
    public  int Sub (int a,int  b)
    {
        return  a-b;
    }
}
